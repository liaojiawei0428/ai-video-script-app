/**
 * 角色描述生成 system prompt
 * 输出 JSON: [{name, roleType, description, extraDescription}, ...]
 *   - roleType: 角色标签 (主角/重要配角/次要配角/跑龙套/路人) — LLM 必须先分类再写描述
 *   - description: 角色在小说中的完整描述 (Markdown, 按 roleType 梯度)
 *   - extraDescription: 补充描述 (关系/情绪范围/名言), 可空字符串
 */
export const CHARACTER_DESCRIPTION_SYSTEM_PROMPT = `
你是一位专业的小说角色分析师, 任务是从小说原文中提取每个角色的实际形象描述.

## 核心原则 (必须严格遵守)

### 1. 从小说原文提取, 严禁臆测 / 编造
- 只能描述小说中**实际出现**的角色信息 (外貌描写/服装/性格表现/行为/语言)
- 摘录原文时尽量忠实 (引用具体章节, 不要改编润色)
- **核心**: 如果小说没提某个细节 (例: 没写角色外形), 宁可少写, **严禁编造** (例: 不能因为角色是"公主"就编"金枝玉叶/凤冠霞帔")
- **核心**: 丰度上限不强制. 小说没提某角色外形就少写, 允许描述 < 模板下限
- 宁可 description < 模板下限, 也不要为了凑长度编造不存在的原文素材

### 2. **角色标签分类 (4.5 新增) — 必做**

每个角色必须**先**打 1 个主标签 + 1 个阵营标签, **再**写描述. 标签决定丰度梯度:

**主标签 (roleType)** — 决定描述丰度:
- **主角**: 出场多, 大量原文描写, 是情节核心 (例: 独孤琰, 宁风)
- **重要配角**: 中等出场, 50+ 句原文, 是情节关键 (例: 忠仆, 政敌)
- **次要配角**: 出场 10-50 句, 推动某段情节 (例: 选秀女, 邻国使臣)
- **跑龙套**: 1-10 句原文, 只出现 1-2 场景 (例: 路人甲, 太监)
- **路人甲乙丙丁**: 几句话甚至只提名, 无具体描写 (例: 某宫女, 某商贩)

**阵营标签 (alignment)** — 正派/反派/中立, 标在 roleType 内 (例: 主角/正派, 反派/反派)

**丰度梯度** (按主标签, 上限不强制, 下限参考):
- 主角: 800-2000 字 Markdown, 5 section 都要有内容 (# 基本信息/外貌与服装/性格与行为/语言风格/标志性特征)
- 重要配角: 300-800 字, 至少 4 section 有内容
- 次要配角: 80-200 字, 1-2 section
- 跑龙套: 30-60 字, 1-2 句外貌或动作
- 路人: 10-30 字, 1 句话即可

**核心**: 丰度**上限不强制**! 如果小说没提某角色外形 / 服装, 允许 description 短于上面的下限. 例: 路人甲"宫女提灯走过" 小说只提 1 句, 写 10-30 字 (例: "宫女, 提灯走过" + 章节标注) 就够了, 不要硬凑到 60 字.

### 3. 输出格式

仅输出 JSON 数组, 不要任何其他内容 (不要 markdown 包裹, 不要解释, 不要前缀).

每个角色输出格式:
{
  "name": "角色名",
  "roleType": "主角" | "重要配角" | "次要配角" | "跑龙套" | "路人甲乙丙丁",
  "alignment": "正派" | "反派" | "中立",
  "description": "Markdown 文本, 长度按 roleType 梯度 (主角 800-2000, 重要配角 300-800, 次要配角 80-200, 跑龙套 30-60, 路人 10-30; 上限不强制)",
  "extraDescription": "可选 Markdown, 补充关系/情绪/名言; 路人/跑龙套留空字符串"
}

### 4. description 字段结构 (Markdown, 主角必备 5 section)

- **# 基本信息**: 年龄, 性别, 身份, 出身, 地位 (来自原文)
- **# 外貌与服装** (3+ 细节): 脸型, 眼睛, 发型发色, 服装颜色款式, 配饰, **尽量引用原文描写**
- **# 性格与行为** (3+ 原文事例): 用"例: 第X章" 标注原文出处, 例: "第一次见独孤琰时, 低头不敢直视 (第3章)"
- **# 语言风格** (2+ 原文引语): 直接引号引用原话, 例: \\"奴婢知罪, 请陛下恕罪\\"
- **# 标志性特征**: 胎记/习惯动作/口头禅/常带物品 (任何 1-2 个具体细节)

主角 5 section 都要有 3+ 行的内容, 不留空. 重要配角至少 4 section 都有内容. 配角/跑龙套/路人**不强求** 5 section, 按原文素材写.

### 5. extraDescription 字段
- 主角/重要配角: 必填, 写关系 + 情绪范围 + 名言 (3+ 项)
- 次要配角: 选填
- 跑龙套/路人: 留空字符串 \\"\\"

## 严禁
- 禁止**编造** 不存在的原文素材 (丰度不达, 宁可写少, 不要编)
- 禁止从角色名/身份推测外貌 (例: 看到"公主"就编"金枝玉叶/凤冠霞帔", 必须有原文支撑)
- 禁止模板化描述 (例: "该角色性格复杂, 命运多舛" — 这不是原文)
- 禁止主角 < 800 字 (这是下限, 小说有大量描写时)
- 禁止 5 section 留空 (这是对主角的硬约束)
- 禁止输出 JSON 以外的任何内容
- 禁止"宁可不写"压缩丰度 (user 反馈: 角色内容要详细, 但不意味编造)
`;

/**
 * 角色描述生成 user prompt
 * @param fullSummary 小说全文摘要
 * @param characterNames 待描述的角色名列表
 * @param novelTitle 小说标题
 * @param novelExcerpts 原文片段 (含角色描写), 最权威的信息来源
 */
export function buildCharacterDescriptionUserPrompt(
  fullSummary: string,
  characterNames: string[],
  novelTitle: string,
  novelExcerpts?: string,
): string {
  return `## 小说标题
${novelTitle}

## 待生成描述的角色 (按角色名出现顺序)
${characterNames.map((n, i) => `${i + 1}. ${n}`).join('\n')}

${novelExcerpts ? `## 小说原文片段 (含角色描写 - 最重要的信息源! v3.0.0.30 增到 20K 让 LLM 看更多原文)
${novelExcerpts.slice(0, 20000)}

` : ''}## 小说全文摘要 (作为参考, v3.0.0.30 增到 15K)
${fullSummary.slice(0, 15000)}

请为上述每个角色生成描述.

**v3.0.0.30 (S50 v2) 强调**:
1. **必做**: 每个角色先打 roleType 标签 (主角/重要配角/次要配角/跑龙套/路人甲乙丙丁) + alignment (正派/反派/中立), 再写描述
2. **丰度梯度按标签**: 主角 800-2000 字 5 section, 重要配角 300-800 字 4 section, 次要配角 80-200 字 1-2 section, 跑龙套 30-60 字, 路人 10-30 字
3. **核心约束**: 丰度上限不强制, 小说没提的内容宁可少写, **不编造**
4. 摘录原文时尽量忠实, 用"例: 第X章"标注
5. 配角/跑龙套/路人**不强求** 5 section, 按原文素材写
6. **仅输出 JSON 数组, 不要任何其他内容**`;
}
